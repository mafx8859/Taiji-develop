package com.taiji.jilin.common.entity.data;

/**
 * @author 单继重
 * @since 2017/12/15 12:04
 */
public class Coach {
    private int id;

    private String name;

    private String couchId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCouchId() {
        return couchId;
    }

    public void setCouchId(String couchId) {
        this.couchId = couchId;
    }
}
