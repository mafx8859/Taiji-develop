package com.taiji.jilin.common.entity.data;

/**
 * @author 单继重
 * @since 2017/12/15 12:03
 */
public class Grade {
    private int id;

    private String name;

    private String gradeId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGradeId() {
        return gradeId;
    }

    public void setGradeId(String gradeId) {
        this.gradeId = gradeId;
    }
}
